pub fn task_a(input: &str) -> usize {
    let unique_digits: [usize; 4] = [2, 4, 3, 7];
    return input.lines()
        .map(|line| line.split(" | ").last().unwrap())
        .map(|output| output.split(" ").collect::<Vec<&str>>())
        .flatten()
        .map(|x| x.len())
        .filter(|str_len| unique_digits.contains(str_len))
        .count();
}

